### 3. **Product of Array Except Self**
   - **Problem**: Given an integer array `nums`, return an array `answer` such that `answer[i]` is equal to the product of all the elements of `nums` except `nums[i]`. You must solve it without using division and in `O(n)` time.
   - **Example**:
     ```python
     Input: [1, 2, 3, 4]
     Output: [24, 12, 8, 6]
     ```

### 4. **Sort Colors (Dutch National Flag Problem)**
   - **Problem**: Given an array with `n` objects colored red, white, or blue, sort them in-place so that objects of the same color are adjacent, with the colors in the order red, white, and blue.
     - You could use the integers `0`, `1`, and `2` to represent red, white, and blue.
   - **Example**:
     ```python
     Input: [2, 0, 2, 1, 1, 0]
     Output: [0, 0, 1, 1, 2, 2]
     ```

### 5. **Find All Anagrams in a String**
   - **Problem**: Given a string `s` and a string `p`, return the start indices of all the anagrams of `p` in `s`.
   - **Example**:
     ```python
     Input: s = "cbaebabacd", p = "abc"
     Output: [0, 6]
     ```

### 6. **Binary Tree Level Order Traversal**
   - **Problem**: Given the `root` of a binary tree, return the level order traversal of its nodes' values (i.e., from left to right, level by level).
   - **Example**:
     ```python
     Input: [3, 9, 20, None, None, 15, 7]
     Output: [[3], [9, 20], [15, 7]]
     ```

### 7. **Evaluate Reverse Polish Notation**
   - **Problem**: Evaluate the value of an arithmetic expression in Reverse Polish Notation (RPN). Valid operators are `+`, `-`, `*`, and `/`. Each operand may be an integer or another expression.
   - **Example**:
     ```python
     Input: ["2", "1", "+", "3", "*"]
     Output: 9
     Explanation: ((2 + 1) * 3) = 9
     ```

